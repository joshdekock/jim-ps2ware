void SMS_EEInit ( void ) {

 unsigned char lROMVer[ 16 ];
 DMAC*         lpDMAC = ( DMAC* )0x1000E000;
 int           lFD;

 lpDMAC -> m_CTRL = DMA_SET_CTRL( 1, D_CTRL_RELE_ON, D_CTRL_MFD_OFF, D_CTRL_STS_UNSPEC, D_CTRL_STD_OFF, D_CTRL_RCYC_8 );
 lpDMAC -> m_STAT = 0;
 lpDMAC -> m_PCR  = 0;
 lpDMAC -> m_SQWC = 0;
 lpDMAC -> m_RBSR = 0;
 lpDMAC -> m_RBOR = 0;

 ChangeThreadPriority (  GetThreadId (), 64  );
 FlushCache ( 0 );

 lFD = fioOpen ( s_pROMVER, O_RDONLY );
 fioRead (  lFD, lROMVer, sizeof ( lROMVer )  );
 fioClose ( lFD );

 g_pBXDATASYS[ 6 ] = lROMVer[ 4 ] == 'E' ? 'E' : ( lROMVer[ 4 ] == 'J' ? 'I' : 'A');

}  /* end SMS_EEInit */

void _exec_error ( void ) {

 Exit ( 0 );

}  /* end _exec_error */

void SMS_EExec ( char* apPath ) {

 __asm__ __volatile__(
  ".set noreorder\n\t"
  "jal  SMS_IOPReset\n\t"
  "move $s0, $a0\n\t"
  "lui  $a0, 0x0100\n\t"
  "la   $a1, s_Loader\n\t"
  "jal  memcpy\n\t"
  "li   $a2, %0\n\t"
  "jal  FlushCache\n\t"
  "li   $a0, 0\n\t"
  "jal  FlushCache\n\t"
  "li   $a0, 2\n\t"
  "la   $a1, _exec_error\n\t"
  ".set reorder\n\t"
  "lui  $ra, 0x0100\n\t"
  "subu $sp, $ra, 16\n\t"
  "move $a0, $s0\n\t"
  :: "n"(  sizeof ( s_Loader )  )
 );

}  /* end SMS_EExec */
